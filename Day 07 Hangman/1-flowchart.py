# Flow Chart Programming of Hangman Game
## Start
## Generate a random word from the word list
## Generate as many blanks as the letters of the word
## Ask the user to guess a letter
## Is guessed letter in the word?
### Yes -> Fill the blanks with the letter -> Are all the blanks filled?
#### Yes -> Win the game
#### No -> Go back to line 4
### No -> Lose a life -> Are there any lives left?
#### Yes -> Go back to line 4
#### No -> Lose the game
## End
