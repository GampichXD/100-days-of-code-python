# Boostrap
# Bootstrap is one of the most popular CSS frameworks for developing responsive and mobile-first websites.
# is open-source and free to use.
# other CSS frameworks are:
# 1. Foundation
# 2. Bulma
# 3. Tailwind CSS
# 4. Materialize CSS
# CSS framworks make class-overload.
# How to use Bootstrap?
# 1. Include the Bootstrap CSS file in the <head> section of your HTML document.
# 2. Include the Bootstrap JS file at the end of your HTML document, just before the closing </body> tag.
# 3. Use Bootstrap classes in your HTML elements to apply styles and layout.
# 4. Customize Bootstrap using your own CSS if needed.

# Boostrap Layout
# 12 column system
# Bootstrap uses a 12-column grid system to create responsive layouts.
# div container
# div row
# div col
# auto fit -> col
# sized columns --> col-1, col-2, col-3, col-4, col-5, col-6, col-7, col-8, col-9, col-10, col-11, col-12 (boostrap devides the screen into 12 equal parts)
# Boostrap breakpoints
# xs - extra small devices (phones, <576px)
# sm - small devices (tablets, ≥576px)
# md - medium devices (desktops, ≥768px)
# lg - large devices (desktops, ≥992px)
# xl - extra large devices (large desktops, ≥1200px)
# xxl - extra extra large devices (larger desktops, ≥1400px)
# Boostrap don't need @media queries
# example: col-sm-4 col-md-6 col-lg-8
# Multiple breakpoints
# example: col-sm-12 col-md-8 col-lg-4
# Mix and match
# example: col-sm-6 col-md-4 col-lg-3
# col --> adjusts to the screen size

