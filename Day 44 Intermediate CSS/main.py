# Font Size
# 1px (pixel) = 1/96 inch = 0.75pt = 0.0264583333 cm = 0.26 mm
# 1 pt (point) = 1/72 inch = 0.352778 mm = 0.0352778 cm = 0.0138889 in (when you use size of your font in ms word)

# 1 em =  100% of parent size (realtive size). If you set font size to 1.5em, it will be 1.5 * parent font size = 1.5*p.

# 1 rem = 100% of root size (realtive size). If you set font size to 1.5rem, it will be 1.5 * root font size = 1.5*root <html tag>.

